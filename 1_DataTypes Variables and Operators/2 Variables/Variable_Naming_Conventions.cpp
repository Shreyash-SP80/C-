
// Variable Naming Conventions
// Naming conventions improve code readability. Common styles:

// 1. Camel Case
//  - First letter lowercase, subsequent words capitalized.
//  - Used in Java, JavaScript, and C++ for variables.
int studentAge;
float accountBalance;


// 2. Pascal Case
//  - First letter of every word capitalized.
//  - Used for class names in C++.
class StudentDetails;


// 3. Snake Case
//  - Words separated by underscores (_).
//  - Common in C and C++ for variables and functions.
int student_age;
float account_balance;  


// 4. Hungarian Notation (Less Common)
// Prefixes indicate data type.
int iAge;       // 'i' for integer
float fBalance; // 'f' for float